    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
        String NIS=jTextNIS.getText();
        String Nama=jTextNama.getText();
        String Kelas=jTextKelas.getText();
        String Jurusan=jComboBoxJurusan.getSelectedItem().toString();

        if (NIS.isEmpty() ) {
            JOptionPane.showMessageDialog(null,"NIS tidak boleh kosong");
            jTextNIS.requestFocus();
        }else if (Nama.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Nama tidak boleh kosong");
            jTextNama.requestFocus();
        }else if (Kelas.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Kelas tidak boleh kosong");
            jTextKelas.requestFocus();
        }else if (Jurusan.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Jurusan tidak boleh kosong");
            jComboBoxJurusan.requestFocus();
       
        }
        else if (!(new Scanner(Kelas).hasNextInt())) {
            JOptionPane.showMessageDialog(null,"Kelas harus angka");
            jTextKelas.requestFocus();
        
        }else{
            try{
                Connection conn=(Connection)Koneksi.KoneksiDB();
                Statement stt=conn.createStatement();
                stt.executeUpdate("insert into Siswi(NIS,Nama,Kelas,Jurusan)"+"VALUES('"+NIS+"','"+Nama+"','"+Kelas+"','"+Jurusan+"')");
                BersihData();
                tampilData();
                SetEditOff();
                JOptionPane.showMessageDialog(this,"Data berhasil disimpan","Success",JOptionPane.INFORMATION_MESSAGE);
            } catch(SQLException e){
                JOptionPane.showMessageDialog(this,"Simpan data gagal\n"+e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

