    private void tampilData(){
        try{
            Object[] judul_kolom = {"NIS", "Nama", "Kelas", "Jurusan"};
            tabModel=new DefaultTableModel(null,judul_kolom);
            TabelSiswi.setModel(tabModel);
            
            Connection conn=(Connection)Koneksi.KoneksiDB();
            Statement stt=conn.createStatement();
            tabModel.getDataVector().removeAllElements();
            
            Rssiswi=stt.executeQuery("SELECT * from siswi ");  
            while(Rssiswi.next()){
                Object[] data={
                    Rssiswi.getString("NIS"),
                    Rssiswi.getString("Nama"),
                    Rssiswi.getString("Kelas"),
                    Rssiswi.getString("Jurusan"),        
                };
               tabModel.addRow(data);
            }                
        } catch (Exception ex) {
        System.err.println(ex.getMessage());
        }
        // disable button
        jButtonSave.setEnabled(false);
        jButtonUpdate.setEnabled(false);
        jButtonDelete.setEnabled(false);
        jButtonCancel.setEnabled(false);
    }
        
         //menampilkan data ke form saat data pada tabel di klik 
   void tblKeForm(){
        jTextNIS.setText(tabModel.getValueAt(TabelSiswi.getSelectedRow(),0)+"");
        jTextNama.setText(tabModel.getValueAt(TabelSiswi.getSelectedRow(),1)+"");
        jTextKelas.setText(tabModel.getValueAt(TabelSiswi.getSelectedRow(),2)+"");
        jComboBoxJurusan.setSelectedItem(tabModel.getValueAt(TabelSiswi.getSelectedRow(),3)+"");
        
        
        jButtonUpdate.setEnabled(true);
        jButtonDelete.setEnabled(true);
        jButtonCancel.setEnabled(true);
        jButtonSave.setEnabled(false);
    }
   
     
